    void atualizarDados(){
        
        FinanceiroVO fvo = FinanceiroVO.getInstancia();
        //zera para nao conter lbl sem valor
        fvo.setQtdinscricoes(0);
        fvo.setQtdpatrocinadores(0);
        fvo.setQtdentrada(0);
        fvo.setQtdsaida(0);
        fvo.setQtdlucro(0);
        
        FinanceiroRN frn = FinanceiroRN.getInstancia();
        String mensagem = frn.atualizaCampos(fvo);
        
        lblQtdInscricoes.setText(Double.toString(fvo.getQtdinscricoes()));
        lblQtdPatrocinadores.setText(Double.toString(fvo.getQtdpatrocinadores()));
        lblQtdEntrada.setText(Double.toString(fvo.getQtdentrada()));
        lblQtdSaida.setText(Double.toString(fvo.getQtdsaida()));
        lblQtdLucro.setText(Double.toString(fvo.getQtdlucro()));
        
    }
    
    void fazerBusca(){
        
        FinanceiroVO fvo = FinanceiroVO.getInstancia();
        fvo.dtm.setNumRows(0);
        
        FinanceiroRN frn = FinanceiroRN.getInstancia();
        String mensagem = frn.buscarGastos(fvo);
        
    }
    
